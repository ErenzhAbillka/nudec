/***********************************
*舵机设定
***********************************/
void servoOneDown(float Angle)	//A1			
{
	float adjustedAngle = Angle/ 270* 2000 + 500; // 将范围 0 到 270 映射到 500 到 2500
    PWM1_SetCompare2(adjustedAngle);
}

void servoOneUp(float Angle)	//A2			
{
	float adjustedAngle = Angle/ 270* 2000 + 500; // 将范围 0 到 270 映射到 500 到 2500
    PWM1_SetCompare3(adjustedAngle);
}



